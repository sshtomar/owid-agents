import marimo

app = marimo.App(width="medium")


@app.cell
def _():
    import marimo as mo
    return (mo,)


@app.cell
def _(mo):
    mo.md(
        """
        # Who Burns, Who Breathes

        CO2 emissions from fuel combustion (millions of tonnes), 2000-2022, for the top 10 emitting countries. Traces the dramatic rise of Asian economies and the slow decline of Western industrial emitters.
        """
    )
    return


@app.cell
def _(mo):
    mo.md(
        """
        ## Data Sources

        - **Carbon dioxide emissions from fuel combustion (millions of tonnes)** (un-sdg)  
  Carbon dioxide emissions from fuel combustion (millions of tonnes) from un-sdg  
  Source: [https://unstats.un.org/sdgs/dataportal/database](https://unstats.un.org/sdgs/dataportal/database)
        """
    )
    return


@app.cell
def _():
    import json
    import urllib.request
    import pandas as pd
    import altair as alt
    return json, urllib, pd, alt


@app.cell
def _(json, urllib, pd, mo):
    _path = mo.notebook_location() / "public" / "catalog" / "datasets" / "sdg--9-4-1--EN_ATM_CO2.json"
    try:
        _text = _path.read_text()
    except AttributeError:
        _text = urllib.request.urlopen(str(_path)).read().decode()
    _raw = json.loads(_text)
    meta_0 = _raw["meta"]
    df_0 = pd.DataFrame(_raw["data"])
    df_0 = df_0.dropna(subset=["value"])
    mo.md(f"**{meta_0['title']}** -- {len(df_0)} rows, {df_0['countryName'].nunique()} countries, {df_0['year'].min()}--{df_0['year'].max()}")
    mo.ui.table(df_0.head(20))
    return (df_0, meta_0)


@app.cell
def _(mo):
    mo.md(
        """
        ## Transformation

        For a time-series view we filter to the top countries by data completeness, then plot each country as a separate line over time.
        """
    )
    return


@app.cell
def _(df_0, pd):
    _counts = df_0.groupby("countryName").size().reset_index(name="n")
    _top = _counts.nlargest(8, "n")["countryName"]
    transformed = df_0[df_0["countryName"].isin(_top)].copy()
    transformed = transformed.sort_values(["countryName", "year"])
    transformed
    return (transformed,)


@app.cell
def _(transformed, alt, mo):
    _chart = (
        alt.Chart(transformed)
        .mark_line(point=True)
        .encode(
            x=alt.X("year:O", title="Year"),
            y=alt.Y("value:Q", title="Value"),
            color=alt.Color("countryName:N", title="Country"),
            tooltip=["countryName", "year", "value"],
        )
        .properties(width=700, height=400)
    )
    mo.ui.altair_chart(_chart)
    return


@app.cell
def _(mo):
    mo.md(
        """
        ## Key Insights

        - China's emissions tripled from ~3,200 Mt in 2001 to over 10,500 Mt in 2021, overtaking the United States around 2006
        - The United States has fallen steadily from a peak of ~5,700 Mt in 2005 to ~4,550 Mt in 2021, a 20% reduction
        - India is the only top-10 emitter still on a steep upward trajectory, more than doubling from 1,000 Mt to 2,500 Mt
        - Germany cut emissions by nearly 25% over the period, the sharpest decline among large economies shown
        """
    )
    return


if __name__ == "__main__":
    app.run()
